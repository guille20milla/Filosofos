    /**
     * Metodo que devuelve true si el numero escrito esta entre 3 y 6, ambos inlcuidos
     * @param numero sacado del TextField
     * @return boolean
     */
    private boolean comprobar(String numero){
        String patron="([3-6])";
        Pattern p = Pattern.compile(patron);
        Matcher m = p.matcher(numero);
        if(m.matches()){
            return true;
        }else{return false;}
    }

